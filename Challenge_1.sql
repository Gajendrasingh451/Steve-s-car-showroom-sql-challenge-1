## What are the details of all cars purchased in the year 2022?##

select c.make,c.type,c.style,c.cost_$,s.purchase_date from cars as c
inner join sales as s
on c.car_id=s.car_id
where year(purchase_date)='2022';

##what is the total number of cars sold by each salesperson##

select s.salesman_id,s.name,count(sa.sale_id) as total_sales from salespersons as s
inner join sales as sa
on s.salesman_id=sa.salesman_id
group by s.salesman_id,s.name
order by total_sales desc;

## What is the total revenue generated by each salesperson ##

select s.salesman_id,s.name,sum(c.cost_$) as total_revenue from salespersons as s
inner join  sales as sa
on s.salesman_id=sa.salesman_id
inner join cars as c
on c.car_id=sa.car_id
group by s.salesman_id,s.name
order by sum(c.cost_$) desc;

##  What are the details of the cars sold by each salesperson ##

